-- Режим времени события (соответствует ORM-модели Event.time_mode).
-- Разделяет события на три сценария отображения/видимости:
--   'range'   — известны начало и конец (показываем "HH:MM–HH:MM", скрываем по ends_at)
--   'start'   — известно только начало (показываем "HH:MM", скрываем через 3 часа после старта)
--   'all_day' — событие "весь день" (старт 09:00, конец 21:00 по Бали; рендерим "Весь день")
-- Для не-парсерных событий остаётся NULL (трактуется как start/range по наличию ends_at).
-- Колонка ends_at уже существует. Идемпотентно: безопасно применять повторно.

ALTER TABLE events
ADD COLUMN IF NOT EXISTS time_mode VARCHAR(16);
